%-------------------------------------------------------------------------------
%
% Write the boundary points to a geo file
%
%------------------------------------------------------------------------------- 
function write_edges2geo(prfx,Es,ios)   
whos
%-------------------------------------------------------------------------------
% Open the geo file
fid = fopen([prfx,'_edges.geo'],'w');
prfx
%-------------------------------------------------------------------------------
% Loop through the points
for n = 1:size(Es,1)
    fprintf(fid, ...
        'Line(%i) = {%i,%i};\n'     , ...
        n+ios(2)                    , ...
        Es(n,1)+ios(1)              , ...
        Es(n,2)+ios(1) );
end
n

%-------------------------------------------------------------------------------
% Close the file
fclose(fid);